void checkside(){
	stop();
	HCSR04_Read();
	for(i=72;i<=216;i++){
		htim2.Instance->CCR1 = i;
		HAL_Delay(50);
	}
	HAL_Delay(2000);
	distance_L = Distance;
	for(i=216;i>=18;i--){
		htim2.Instance->CCR1 = i;
		HAL_Delay(50);
	}
	HAL_Delay(2000);
	distance_R = Distance;
	for(i=18;i<=72;i++){
		htim2.Instance->CCR1 = i;
		HAL_Delay(50);
	}
	HAL_Delay(2000);
	compareDistance();
}
